# ternary oprator
# X if condition is True else Y

# check to see if number is positive

num = 5

print("Positive" if num > 0 else "Negitive")  # Positive


# check if num is even or odd

print("Even" if num % 2 == 0 else "Odd")  # odd

# find max between 2 numebrs
a = 6
b = 7
max_num = a if a > b else b
print(f"The bigger number is: {max_num}")
# The bigger number is: 7


# check if adult
age = 33
print("Adult" if age > 18 else "Minor")  # adult
